
module Sugar where

import Exp

desugarVar :: Var -> IndexedVar
desugarVar v = makeIndexedVar (getVar v)

-- >>> desugarVar (Var "x")
-- IndexedVar {ivName = "x", ivCount = 0}

sugarVar :: IndexedVar -> Var
sugarVar v = Var (ivName v) 

-- >>> sugarVar (IndexedVar "x" 0)
-- Var {getVar = "x"}

-- >>> sugarVar (IndexedVar "x" 3)
-- Var {getVar = "x_3"}

consExp, nilExp, zeroExp, succExp, fixExp :: Exp
consExp = X (makeIndexedVar ":")  -- : :: a -> List a -> List a  list constructor
nilExp = X (makeIndexedVar "Nil") -- Nil :: List a               empty list
zeroExp = X (makeIndexedVar "Z")  -- Z :: Natural                zero
succExp = X (makeIndexedVar "S")  -- S :: Natural -> Natural     successor
fixExp = X (makeIndexedVar "fix") -- fix :: (a -> a) -> a        fixpoint fn.

desugarExp :: ComplexExp -> Exp
desugarExp (CX x) = X (desugarVar x)
desugarExp (CLam v x) = Lam (desugarVar v) (desugarExp x)
desugarExp (CApp x y) = App (desugarExp x) (desugarExp y)
desugarExp (List l) = case l of
    [] -> nilExp
    a:rest -> App (App consExp (desugarExp a)) (desugarExp (List rest))
desugarExp (Nat n) = case n of
    0 -> zeroExp
    x -> App succExp (desugarExp (Nat (n-1)))
desugarExp (Let v x y) = App (Lam (desugarVar v) (desugarExp y)) (desugarExp x)
desugarExp (LetRec v x y) = App (Lam (desugarVar v) (desugarExp y)) (App fixExp (Lam (desugarVar v) (desugarExp x)))



