sugarExp :: Exp -> ComplexExp
sugarExp (X v) = CX (sugarVar v)
sugarExp (Lam v x) = CLam (sugarVar v) (sugarExp x)
sugarExp (App x y) = CApp (sugarExp x) (sugarExp y)

